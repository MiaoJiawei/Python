# 新建序列 字符串，列表，元组
name = 'swaroop'
shoplist = ['apple', 'mango', 'carrot', 'banana', 'peach']
zoo = ('python', 'elephent', 'penguin')

# 索引下标 Indexing or Subscription
print('character "swaroop" 0 is', name[0])
print('item "shoplist" 0 is', shoplist[0])
print('item "zoo" 0 is', zoo[0])

# 切片运算 Slicing
print('character "swaroop" 1 to 5 is', name[1:5])
print('character "swaroop" 1 to 5 @ 2 is', name[1:5:2])
print('item "shoplist" 1 to 5 is', shoplist[1:5])
print('item "shoplist" 1 to 5 @ 2 is', shoplist[1:5:2])
print('item "zoo" 0 to -1 is', zoo[0:-1])
print('item "zoo" 0 to -1 @ 2 is', zoo[0:-1:2])